package main

import "fmt"

func main() {

	var intVar int = 10
	var floatVar float64 = 20.5
	var stringVar string = "Bump"
	var boolVar bool = true

	fmt.Println("Значение переменной int:", intVar)
	fmt.Println("Значение переменной float64:", floatVar)
	fmt.Println("Значение переменной string:", stringVar)
	fmt.Println("Значение переменной bool:", boolVar)
}
